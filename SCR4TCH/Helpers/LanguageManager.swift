enum FontSizePreference: String, CaseIterable {
    case small = "small"
    case large = "large"
    
    var name: String {
        switch self {
        case .small: return "Small"
        case .large: return "Large"
        }
    }
    
    var icon: String {
        switch self {
        case .small: return "textformat.size.smaller"
        case .large: return "textformat.size.larger"
        }
    }
    
    var multiplier: CGFloat {
        switch self {
        case .small: return 0.9
        case .large: return 1.1
        }
    }
}

class LanguageManager: ObservableObject {
    static let shared = LanguageManager()
    
    @Published var currentLanguage: SupportedLanguage = .english
    @Published var currentFontSize: FontSizePreference = .small
    
    private let userDefaults = UserDefaults.standard
    private let languageKey = "selectedLanguage"
    private let fontSizeKey = "selectedFontSize"
    
    private init() {
        loadSettings()
    }
    
    func setLanguage(_ language: SupportedLanguage) {
        currentLanguage = language
        userDefaults.set(language.rawValue, forKey: languageKey)
    }
    
    func setFontSize(_ fontSize: FontSizePreference) {
        currentFontSize = fontSize
        userDefaults.set(fontSize.rawValue, forKey: fontSizeKey)
    }
    
    private func loadSettings() {
        if let savedLanguage = userDefaults.string(forKey: languageKey),
           let language = SupportedLanguage(rawValue: savedLanguage) {
            currentLanguage = language
        }
        
        if let savedFontSize = userDefaults.string(forKey: fontSizeKey),
           let fontSize = FontSizePreference(rawValue: savedFontSize) {
            currentFontSize = fontSize
        }
    }
    
    func translate(_ key: String) -> String {
        return translations[currentLanguage]?[key] ?? key
    }
    
    func scaledFontSize(_ baseSize: CGFloat) -> CGFloat {
        return baseSize * currentFontSize.multiplier
    }
    
    var isRTL: Bool {
        return currentLanguage.isRTL
    }
}

